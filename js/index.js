function multiplyBy()
{
    num1 = document.getElementById("firstNumber").Value;
    num2 = document.getElementById("secondNumber").Value;
    document.getElementById("result").innerHTML = num1 * num2;

}
function devideBy()
{
    num1 = document.getElementById("firstNumber").Value;
    num2 = document.getElementById("secondNumber").Value;
    document.getElementById("result").innerHTML = num1 / num2;

}